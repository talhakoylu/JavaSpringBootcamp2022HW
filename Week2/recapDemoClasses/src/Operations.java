public class Operations {

    public int addition(int num1, int num2){
        return num1 + num2;
    }

    public int subtraction(int num1, int num2){
        return num1 - num2;
    }

    public int multiplication(int num1, int num2){
        return num1 * num2;
    }

    public double division(int num1, int num2){
        return num1 / num2;
    }

}
